# 联想冒泡排序-升序
l = [2,12,68,45,99,6,32,888]
for n in range(1,len(l)):
    for i in range(1,len(l)-n):
        if l[i] > l[i + 1]:
            l[i], l[i + 1] = l[i + 1], l[i]
print(l)

