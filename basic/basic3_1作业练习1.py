'''
判断是否为闰年
提示:
输入一个有效的年份（如：2019），判断是否为闰年
如果是闰年，则打印“2019年是闰年”；否则打印“2019年不是闰年”
普通年能被4整除且不能被100整除的为闰年
世纪年能被400整除的是闰年
'''
year = input("请输入年份：")
year = int(year)
if (year%4==0 and year%100!=0) or year%400==0:
    print("{}是闰年".format(year))
else:
    print("{}不是闰年".format(year))

